const hangulToQwerty = (str: string) => {
	return Array.from(str)
		.map(
			(_) =>
				HANGUL_QWERTY_KEYBOARD_MAP[
					_ as keyof typeof HANGUL_QWERTY_KEYBOARD_MAP
				] || " ",
		)
		.join("");
};

